#include "ft_printf.h"

int	ft_printstr(char *str)
{
	int	len;

	len = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[len])
		len++;
	write (1, str, len);
	return (len);
}
